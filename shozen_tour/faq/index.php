<div class="faq">

<dl>
<dt>予約はいつまでにすればよいですか？</dt>
<dd>
特に期日は設けておりません。<br />
お日にちがお決まりになられた段階で ご予約ください。
</dd>
</dl>

<dl>
<dt>予約方法は？</dt>
<dd>
店頭にお越しいただくか、お電話でバスの空き状況をご確認の上 ご予約ください。
</dd>
</dl>

<dl>
<dt>食事や宿泊の手配もしてくれますか？</dt>
<dd>
はい、それらの手配も当社で代行して行うことができます。
</dd>
</dl>

<dl>
<dt>旅行当日の支払いなどは乗務員が代行してくれますか？</dt>
<dd>
支払いの代行は可能です。<br />
ただし、添乗員などがいない場合はお客さまに支払いをお願いする場合がございます。
</dd>
</dl>

<dl>
