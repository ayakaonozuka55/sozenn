<h2 id="faq02">巡礼</h2>

<div class="faq">

<dl>
<dt>巡礼ツアーの意義ってなんですか？</dt>
<dd>
簡単に言うと先祖供養と願い事の旅です。<br />
ご自身のことや身近な人、いつもお世話になっている人のことを想ってお参りをしてください。<br />
また、様々な体験を通してお参り仲間との絆や新たな発見ができるのも巡礼ツアーの魅力です。
</dd>
</dl>

<dl>
<dt>お参りについて全く分からないのですが？</dt>
<dd>
同じようなお客さまもたくさんいらっしゃいます。<br />
初めは分からなくとも、全日程に同行する先達(お寺専門のガイド)が分かりやすく説明してくださいます。
</dd>
</dl>

<dl>
<dt>足腰が心配なのですが？</dt>
<dd>
タクシーやマイクロバスを使ってできるだけお客さまが楽にお参りできるよう配慮はしております。
</dd>
</dl>

<dl>
<dt>座席の指定はできますか？</dt>
<dd>
指定はできません。ただし特別な理由により、希望がある場合は申し出てください。
</dd>
</dl>

<dl>
<dt>1人参加はできますか？</dt>
<dd>
もちろんできます。2人以上のグループ様も大歓迎です。
</dd>
</dl>

<dl>
<dt>雨でも出発しますか？</dt>
<dd>
天候に関わらず出発します。<br />
台風・大雪・災害などお客さまの安全が確保できない場合などは中止になる場合がございます。<br />
万が一中止の場合は、当社よりご連絡させていただきます。
</dd>
</dl>

<dl>
<dt>旅行代金の支払いはどうすればよいですか？</dt>
<dd>
銀行振り込み・店頭にて現金払いのいずれかでお願いいたします。
</dd>
</dl>

<dl>
<dt>行程表が届かないのですが？</dt>
<dd>
お客さまがご入金いただいてから、<br />
当社で確認し行程表発送まで3～4日かかる場合がございます。<br />
万が一それ以上待っても届かない場合は、お手数ですがお問合せ下さい。
</dd>
</dl>

